def compare_dictionaries(dict1, dict2):
    """
    Compares two dictionaries and returns the differences in keys and values.

    Parameters:
    dict1 (dict): The first dictionary to compare.
    dict2 (dict): The second dictionary to compare.

    Returns:
    dict: A dictionary containing the differences with the following structure:
          {
              'only_in_dict1': {key: value},
              'only_in_dict2': {key: value},
              'different_values': {key: (value_in_dict1, value_in_dict2)}
          }
    """
    differences = {
        'only_in_dict1': {},
        'only_in_dict2': {},
        'different_values': {}
    }

    # Find keys that are only in dict1
    for key in dict1:
        if key not in dict2:
            differences['only_in_dict1'][key] = dict1[key]
        elif dict1[key] != dict2[key]:
            differences['different_values'][key] = f"{dict1[key]} | {dict2[key]}"

    # Find keys that are only in dict2
    for key in dict2:
        if key not in dict1:
            differences['only_in_dict2'][key] = dict2[key]

    return differences

def first_non_none(*args):
    """
    Returns the first non-None value from the given arguments.

    Parameters:
    *args: A variable number of arguments to check.

    Returns:
    The first non-None value, or None if all values are None.
    """
    for arg in args:
        if arg is not None:
            return arg
    return None

